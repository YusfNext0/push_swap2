#include "push_swap.h"

void    sb(p_list **b)
{
    p_list  *temp;

    // if (!b)
    //     exit(0);
    temp = (*b)->next;
    (*b)->next = temp->next;
    temp->next = (*b);
    (*b) = temp;
    write (1, "sb", 2);
}

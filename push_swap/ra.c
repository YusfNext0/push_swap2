#include "push_swap.h"

void    ra(p_list **a)
{
    p_list  *temp;
    
    temp = (*a);
    (*a) = (*a)->next;
    temp->next = NULL;
    
    lstadd_back(a, temp);
    write (1, "ra", 2);
}

#include "push_swap.h"

void    pa(p_list **a, p_list **b)
{
    p_list  *temp;

    if (!(*b))
        return;
    temp = (*b);
    (*b) = (*b)->next;
    temp->next = (*a);
    (*a) = temp;
    write (1, "pa\n", 3);
}
